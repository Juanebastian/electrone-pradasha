    /** 
     * <b>hashPassword()</b>
     * <b>OBJETIVO:</b>Encritrtar con un hash la contrase침a del usuario.
     *
     * @param password Objeto Proveedor que contiene la informaci칩n del nuevo proveedor.
     * @return password contrase침a encryptada.
     */
     private String hashPassword(String password) {
        try {
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte[] hashedBytes = md.digest(password.getBytes());
            StringBuilder sb = new StringBuilder();
            for (byte b : hashedBytes) {
                sb.append(String.format("%02x", b));
            }
            return sb.toString();
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
            return null;
        }
    }//Fin hashPassword 
     
